#include <iostream>
#include <string>

using namespace std;

int main()
{
    string trash;

    int start;
    int a, b, c;

    cin >> trash >> start;
    cin >> a >> trash >> b >> trash >> c;

    int end;
    int x, y, z;

    cin >> trash >> end;
    cin >> x >> trash >> y >> trash >> z;

    cout << a << b << c << endl;
    cout << x << y << z << endl;

    return 0;
}